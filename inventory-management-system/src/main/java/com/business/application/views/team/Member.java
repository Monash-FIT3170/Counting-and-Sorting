package com.business.application.views.team;

public class Member {

    private int id;
    private String img;
    private String name;
    private String store;
    private String email;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }

    public String getName() {
        return name;
    }

    public void setName(String member) {
        this.name = member;
    }

    public String getStore() {
        return store;
    }

    public void setStore(String status) {
        this.store = status;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String date) {
        this.email = date;
    }
}
